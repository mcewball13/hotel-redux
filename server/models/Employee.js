const { Schema, model } = require('mongoose');

const EmployeeSchema = new Schema({
  username: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    match: [/.+@.+\..+/, 'Must match an email address!']
  },
  password: {
    type: String,
    required: true,
    minlength: 5
  },
});

const Employee = model('Employee', EmployeeSchema);

module.exports = Employee;
